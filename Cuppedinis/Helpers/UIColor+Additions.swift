import UIKit

extension UIColor {
    
    @nonobjc class var jadeGreen: UIColor {
        return UIColor(red:0.00, green:0.66, blue:0.42, alpha:1.0)
    }
    
    @nonobjc class var pinkishRed: UIColor {
        return UIColor(red: 237.0 / 255.0, green: 28.0 / 255.0, blue: 36.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var pineGreen: UIColor {
        return UIColor(red:0.00, green:0.47, blue:0.44, alpha:1.0)
    }
    
    @nonobjc class var seaGreen: UIColor {
        return UIColor(red:0.18, green:0.55, blue:0.34, alpha:1.0)
    }

    @nonobjc class var azureBlue: UIColor {
        return UIColor(red:0.00, green:0.50, blue:1.00, alpha:1.0)
    }
    
    @nonobjc class var sapphireBlue: UIColor {
        return UIColor(red:0.06, green:0.32, blue:0.73, alpha:1.0)
    }
    
    @nonobjc class var cyberYellow: UIColor {
        return UIColor(red:1.00, green:0.83, blue:0.00, alpha:1.0)
    }

    @nonobjc class var gold: UIColor {
        return UIColor(red:0.98, green:0.65, blue:0.01, alpha:1.0)
    }
    
}
